package com.bit.day13;

public class Ex06 {

	public static void main(String[] args) {
		
		double su1 = 3.14;
		System.out.println("절대값 "+Math.abs(-123));
		System.out.println("반올림 "+Math.round(su1)); // 반올림
		System.out.println("내림 "+Math.floor(su1)); // 내림
		System.out.println("올림 "+Math.ceil(su1)); // 올림
		
		System.out.println(Math.max(1,3));
		System.out.println(Math.min(1,3));
		
	}

}
